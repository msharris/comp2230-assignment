public class Hotspot {

    private int id;
    private double x;
    private double y;

    public Hotspot(int id, double x, double y) {
        this.id = id;
        this.x = x;
        this.y = y;
    }

    public int getId() {
        return id;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    @Override
    public String toString() {
        return Integer.toString(id);
    }

}
